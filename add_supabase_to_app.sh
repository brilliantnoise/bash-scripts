}

ensure_map_directive() {
  # WebSocket Connection header: upgrade when client sends Upgrade, else close.
  local maps="/etc/nginx/conf.d/00-bash-scripts-ws-map.conf"
  if [[ -f "${maps}" ]] && grep -q 'map \$http_upgrade \$connection_upgrade' "${maps}"; then
    return 0
  fi
  cat > "${maps}" <<'MAPEOF'
# Shared by add_app-style vhosts and Supabase path proxy (bash-scripts)
map $http_upgrade $connection_upgrade {
  default upgrade;
  ''      close;
}
MAPEOF
}

insert_include_into_conf() {
  local conf="$1"
  local domain="$2"
  python3 - "$conf" "$domain" "$MARKER" <<'PY'
import re
import sys
from pathlib import Path

path = Path(sys.argv[1])
domain = sys.argv[2]
marker = sys.argv[3]
text = path.read_text(encoding="utf-8")

if "supabase-kong-proxy.conf" in text:
    print(f"Include already present (snippet refreshed): {path}")
    sys.exit(0)

include_line = f"  include /etc/nginx/snippets/supabase-kong-proxy.conf;  # {marker}\n"
# Match server_name lines that include this host (covers certbot :80 and :443 blocks)
pat = re.compile(
    r"^(\s*server_name\s+[^;\n]*" + re.escape(domain) + r"[^;\n]*;)\s*$",
    re.MULTILINE | re.IGNORECASE,
)
new_text, n = pat.subn(lambda m: m.group(1) + "\n" + include_line, text)
if n == 0:
    print(f"ERROR: No server_name line containing '{domain}' in {path}", file=sys.stderr)
    sys.exit(1)
path.write_text(new_text, encoding="utf-8")
print(f"Inserted Supabase include after {n} server_name line(s): {path}")
PY
}

write_snippet
ensure_map_directive
cp -a "${CONF}" "${CONF}.bak.$(date +%Y%m%d%H%M%S)"
insert_include_into_conf "${CONF}" "${DOMAIN}"

# Preserve webhook paths (unchanged) — they use ^~ and stay in vhost; snippet uses ^~ for API paths.
# Order: nginx chooses longest prefix; ^~ stops search. /auth/v1 etc. win over / for those prefixes.

nginx -t
systemctl reload nginx

echo ""
echo "=========================================="
echo "Supabase path routing added for: ${DOMAIN}"
echo "=========================================="
echo "Config:        ${CONF}"
echo "Snippet:       ${SNIPPET_PATH}"
echo "Kong upstream: 127.0.0.1:${KONG_HTTP_PORT}"
echo ""
echo "Set app env to:"
echo "  NEXT_PUBLIC_SUPABASE_URL=https://${DOMAIN}"
echo "  (use http only if this vhost is HTTP-only — match install_supabase.sh PUBLIC_BASE_URL)"
echo "=========================================="
