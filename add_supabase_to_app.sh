#!/usr/bin/env bash
# add_supabase_to_app.sh — Add reserved Supabase API path routes to an existing nginx vhost
# (created by add_app.sh). Proxies to local Kong (Docker).
#
# Usage:
#   sudo ./add_supabase_to_app.sh <server_name>
# Example:
#   sudo ./add_supabase_to_app.sh app.clientdomain.com
#
# Optional env:
#   NGINX_CONF_DIR=/etc/nginx/conf.d
#   SUPABASE_DOCKER_DIR=/opt/supabase/docker   (read KONG_HTTP_PORT from .env)
#   KONG_HTTP_PORT=8000                        (override)
#
set -euo pipefail

NGINX_CONF_DIR="${NGINX_CONF_DIR:-/etc/nginx/conf.d}"
SUPABASE_DOCKER_DIR="${SUPABASE_DOCKER_DIR:-/opt/supabase/docker}"
SNIPPET_PATH="/etc/nginx/snippets/supabase-kong-proxy.conf"
MARKER="bash-scripts-supabase-include"

DOMAIN="${1:-}"
if [[ -z "${DOMAIN}" ]]; then
  read -rp "Nginx server_name (e.g. app.example.com): " DOMAIN
fi
[[ -n "${DOMAIN}" ]] || { echo "server_name is required"; exit 1; }

if [[ $EUID -ne 0 ]]; then echo "Run as root (sudo)"; exit 1; fi

CONF="${NGINX_CONF_DIR}/${DOMAIN}.conf"
[[ -f "${CONF}" ]] || { echo "Missing ${CONF} — run add_app.sh for this host first."; exit 1; }

KONG_HTTP_PORT="${KONG_HTTP_PORT:-}"
if [[ -z "${KONG_HTTP_PORT}" && -f "${SUPABASE_DOCKER_DIR}/.env" ]]; then
  KONG_HTTP_PORT="$(grep -E '^KONG_HTTP_PORT=' "${SUPABASE_DOCKER_DIR}/.env" | cut -d= -f2- | tr -d '\r' || true)"
fi
: "${KONG_HTTP_PORT:=8000}"

write_snippet() {
